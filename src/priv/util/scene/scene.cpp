static void samplecode()
{
    using namespace GN;
    using namespace GN::scene;

    Scene s;

    SceneRenderer r;

    loadSceneFromFile( s, "media:/scene1/scene.xml" );

    if( r.init() ) return;

    // main rendering loop
    while( 1 )
    {
        s.update();
    }

    // end application
    r.waitForIdle();
}
