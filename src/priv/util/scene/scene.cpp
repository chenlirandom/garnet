static void samplecode()
{
    using namespace GN;
    using namespace GN::scene;

    SceneRenderer        r;
    SceneResourceManager rm;
    Scene                s;

    if( !r.init() ) return;
    if( !rm.init() ) return;

    loadSceneFromFile( s, "media:/scene1/scene.xml" );

    // main rendering loop
    while( notquit() )
    {
        s.frameUpdate();
    }

    // end application
    r.waitForIdle();
}
