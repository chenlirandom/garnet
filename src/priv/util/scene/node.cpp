#include "pch.h"
#include "garnet/GNscene.h"

// *****************************************************************************
// ctor / dtor
// *****************************************************************************

//
//
// -----------------------------------------------------------------------------
GN::scene::SceneNode::SceneNode( Scene & s ) : mScene(s)
{
    mId = s.addNode( this );
    GN_ASSERT( mId > 0 );
}

//
//
// -----------------------------------------------------------------------------
GN::scene::SceneNode::~SceneNode()
{
    mScene.removeObject( this );
}
